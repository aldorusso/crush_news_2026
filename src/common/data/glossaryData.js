// Función para obtener términos por letra
export const getTermsByLetter = (letter) => {
  return glossaryTerms.filter(term =>
    term.term.toUpperCase().startsWith(letter.toUpperCase())
  )
}

// Función para buscar términos
export const searchTerms = (query) => {
  const lowerQuery = query.toLowerCase()
  return glossaryTerms.filter(term =>
    term.term.toLowerCase().includes(lowerQuery) ||
    term.definition.toLowerCase().includes(lowerQuery)
  )
}

// Función para obtener términos por categoría
export const getTermsByCategory = (category) => {
  if (category === "all") return glossaryTerms
  return glossaryTerms.filter(term =>
    term.category.toLowerCase() === category.toLowerCase()
  )
}

// Obtener todas las letras disponibles
export const getAvailableLetters = () => {
  const letters = new Set(glossaryTerms.map(term => term.term[0].toUpperCase()))
  return Array.from(letters).sort()
}

export default glossaryTerms
